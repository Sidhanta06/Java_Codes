package programming_class_4to6;

public class Swaping {
	public static void main(String[] args) {
		int n=5;
		int n1=10;
		int n2=0;
		
		System.out.println(n+" before");
		System.out.println(n1+" before");
		System.out.println("----------------");
		n2=n;
		n=n1;
		n1=n2;
		
		System.out.println(n+" after");
		System.out.println(n1+" after");
	}

}
